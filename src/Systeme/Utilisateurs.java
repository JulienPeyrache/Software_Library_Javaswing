    public void TableEmprunts(){
        String [] emprunts = {"emprunt_id", "emprunt_userid", "emprunt_bookid", "emprunt_date", "emprunt_inout"};
        String [] afficher = new String[6];
        DefaultTableModel model = new DefaultTableModel(null, emprunts);
        String sql = "select * from emprunts";
        
        try{
            Class.forName("org.sqlite.JDBC");
            String url = "jdbc:sqlite:biblio_v1.db";
            String name = "root";
            String pass = "";
            con = DriverManager.getConnection(url, name, pass);
            Statement st = con.createStatement();
            rs = st.executeQuery(sql);
            
            while (rs.next()){
                afficher[0] = rs.getString("emprunt_id");
                afficher[1] = rs.getString("emprunt_userid");
                afficher[2] = rs.getString("emprunt_bookid");
                afficher[3] = rs.getString("emprunt_date");
                afficher[4] = rs.getString("emprunt_inout");
                model.addRow(afficher);  
            }
            jTable1.setModel(model);
            con.close();
        } catch(Exception e){
           e.printStackTrace();
        }
    }
    
